    <script>
    document.addEventListener("DOMContentLoaded", () => {
        // Disable GSAP scroll triggers and timelines on mobile for static loading
        if (window.innerWidth <= 768) {
            document.querySelectorAll(".impact-number").forEach(stat => {
                const target = stat.getAttribute("data-target");
                const prefix = stat.getAttribute("data-prefix") || "";
                const suffix = stat.getAttribute("data-suffix") || "";
                stat.textContent = prefix + target + suffix;
            });
            return;
        }

        // 1. Hero Load Entrance
        const tl = gsap.timeline({ defaults: { ease: "power3.out" } });
        tl.fromTo(".hero-pill", { opacity: 0, y: -20 }, { opacity: 1, y: 0, duration: 0.8 })
          .fromTo(".nosotros-title", { opacity: 0, y: 30 }, { opacity: 1, y: 0, duration: 1 }, "-=0.6")
          .fromTo(".nosotros-sub", { opacity: 0, y: 20 }, { opacity: 1, y: 0, duration: 0.8 }, "-=0.6")
          .fromTo(".hero-buttons .btn-primary, .hero-buttons .btn-secondary", { opacity: 0, scale: 0.95 }, { opacity: 1, scale: 1, duration: 0.6, stagger: 0.15 }, "-=0.5")
          .fromTo(".mini-stat", { opacity: 0, y: 15 }, { opacity: 1, y: 0, duration: 0.6, stagger: 0.1 }, "-=0.4")
          .fromTo(".hero-card-main", { opacity: 0, x: 50 }, { opacity: 1, x: 0, duration: 0.8 }, "-=0.8")
          .fromTo(".floating-card", { opacity: 0, scale: 0.8 }, { opacity: 1, scale: 1, duration: 0.8, stagger: 0.2 }, "-=0.5")
          .fromTo(".hero-blur", { opacity: 0, scale: 0.8 }, { opacity: 0.25, scale: 1, duration: 2, stagger: 0.3 }, "-=1.5");

        // 2. Floating dynamic loops
        gsap.to(".floating-1", {
            y: -15,
            duration: 2.5,
            repeat: -1,
            yoyo: true,
            ease: "power1.inOut"
        });
        gsap.to(".floating-2", {
            y: 15,
            duration: 3,
            repeat: -1,
            yoyo: true,
            ease: "power1.inOut"
        });

        // 3. ScrollTrigger counters
        const impactNumbers = document.querySelectorAll(".impact-number");
        impactNumbers.forEach(stat => {
            const target = parseFloat(stat.getAttribute("data-target"));
            const prefix = stat.getAttribute("data-prefix") || "";
            const suffix = stat.getAttribute("data-suffix") || "";
            const hasComma = stat.getAttribute("data-format") === "comma";

            gsap.fromTo(stat, 
                { textContent: 0 }, 
                { 
                    textContent: target, 
                    duration: 2, 
                    ease: "power2.out",
                    snap: { textContent: 1 },
                    scrollTrigger: {
                        trigger: stat,
                        start: "top 85%",
                        toggleActions: "play none none none"
                    },
                    onUpdate: function() {
                        let val = Math.floor(this.targets()[0].textContent);
                        if (hasComma) {
                            stat.textContent = prefix + val.toLocaleString('en-US') + suffix;
                        } else {
                            stat.textContent = prefix + val + suffix;
                        }
                    }
                }
            );
        });

        // 4. Staggered reveal for About Grid
        gsap.fromTo(".about-card", 
            { opacity: 0, y: 30 },
            { 
                opacity: 1, 
                y: 0, 
                duration: 0.6, 
                stagger: 0.15,
                ease: "power2.out",
                scrollTrigger: {
                    trigger: ".about-grid",
                    start: "top 85%"
                }
            }
        );

        // 5. Mission Vision Cards Staggered Entry
        gsap.fromTo(".mv-card",
            { opacity: 0, scale: 0.95, y: 30 },
            {
                opacity: 1,
                scale: 1,
                y: 0,
                duration: 0.8,
                stagger: 0.2,
                ease: "power2.out",
                scrollTrigger: {
                    trigger: ".mv-grid",
                    start: "top 85%"
                }
            }
        );

        // 6. Timeline steps entry
        gsap.fromTo(".timeline-item",
            { opacity: 0, x: -30 },
            {
                opacity: 1,
                x: 0,
                duration: 0.6,
                stagger: 0.2,
                ease: "power2.out",
                scrollTrigger: {
                    trigger: ".timeline",
                    start: "top 85%"
                }
            }
        );

        // 7. Partners Staggered Entry
        gsap.fromTo(".partner-card",
            { opacity: 0, y: 20 },
            {
                opacity: 1,
                y: 0,
                duration: 0.5,
                stagger: 0.1,
                ease: "power2.out",
                scrollTrigger: {
                    trigger: ".partners-grid",
                    start: "top 85%"
                }
            }
        );

        // Partner cards hover refinement
        const partnerCards = document.querySelectorAll(".partner-card");
        partnerCards.forEach(card => {
            card.addEventListener("mouseenter", () => {
                gsap.to(card, { y: -8, scale: 1.02, boxShadow: "0 12px 30px rgba(2,6,23,.08)", duration: 0.3 });
            });
            card.addEventListener("mouseleave", () => {
                gsap.to(card, { y: 0, scale: 1, boxShadow: "none", duration: 0.3 });
            });
        });

        // 8. CTA Box Zoom-in Entry
        gsap.fromTo(".cta-box",
            { opacity: 0, scale: 0.95 },
            {
                opacity: 1,
                scale: 1,
                duration: 0.8,
                ease: "power3.out",
                scrollTrigger: {
                    trigger: ".cta-box",
                    start: "top 90%"
                }
            }
        );
    });
